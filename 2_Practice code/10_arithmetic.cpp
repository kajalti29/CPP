#include<iostream>
using namespace std;
int main() {
    int a = 10, b = 3;
    cout << "a + b = " << a + b << endl;  // 13
    cout << "a - b = " << a - b << endl;  // 7
    cout << "a * b = " << a * b << endl;  // 30
    cout << "a / b = " << a / b << endl;  // 3
    cout << "a % b = " << a % b << endl;  // 1
    return 0;
}
