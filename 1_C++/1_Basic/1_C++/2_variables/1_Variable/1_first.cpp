// Variable: A name for a memory location.


#include <iostream>
using namespace std;

int main() {
    int age = 20;       // integer variable
    float price = 99.5; // decimal value
    char grade = 'A';   // single character
    bool isPass = true; // true/false

    cout << "Age = " << age << endl;
    cout << "Price = " << price << endl;
    cout << "Grade = " << grade << endl;
    cout << "Pass = " << isPass << endl; // true = 1, false = 0
    return 0;
}
