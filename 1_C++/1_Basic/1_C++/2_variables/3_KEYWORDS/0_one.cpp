// . Keywords & Identifiers

// Keywords → Reserved words (cannot be used as variable names).
// Examples: int, float, if, else, while, return, class, for, switch, break

// Identifiers → Names given to variables, functions, etc.

// Rules:

// Must start with letter or _

// Cannot use spaces or special symbols

// Case-sensitive (Age ≠ age)

// 👉 Example:

int number = 10;    // valid
float marks_1 = 99; // valid
// int for = 5;    ❌ invalid (keyword)