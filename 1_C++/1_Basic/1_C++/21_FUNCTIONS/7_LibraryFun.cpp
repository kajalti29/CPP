#include<iostream>
#include<cmath>
using namespace std;

int main() {
    cout << sqrt(25) << endl;   // 5 (square root)
    cout << pow(2,3) << endl;   // 8 (2^3)
    cout << abs(-10) << endl;   // 10 (absolute value)
    cout << ceil(3.4) << endl;  // 4 (round up)
    cout << floor(3.9) << endl; // 3 (round down)
    return 0;
}
