#include<iostream>
using namespace std;

int main() {
    int x = 5, y = 2;

    cout << x + y << endl;  // Addition
    cout << x - y << endl;  // Subtraction
    cout << x * y << endl;  // Multiplication
    cout << x / y << endl;  // Division


    // यहाँ ध्यान दें ❗ दोनों variables integer हैं, तो result भी integer होगा।
// यानी 5 / 2 = 2 (क्योंकि decimal part ignore हो जाता है)।
} 
