#include<iostream>
using namespace std;

int main(){
    int a = 10;
    int b = 3;
    cout<<"a + b = "<<a+b<<endl;  // Addition
    cout<<"a - b = "<<a-b<<endl;  // Subtraction
    cout<<"a * b = "<<a*b<<endl;  // Multiplication
    cout<<"a / b = "<<a/b<<endl; // integer division
    cout<<"a % b = "<<a%b<<endl; // modulus operator

return 0;
}